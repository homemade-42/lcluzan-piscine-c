void	ft_rev_int_tab(int *tab, int size)
{
	int	tmp;
	int i;

	i = 0;
	size = size -1;
	while (i < size)
	{
		tmp = tab[i];
		tab[i] = tab[size];
		tab[size] = tmp;
		i++;
		size--;
	}
}
